'''
1.Arithmetic Operators
  (i)    + = For addition
  (ii)   - = For Subtraction
  (iii)  / = For Division
  (iv)   * = For Multiplication
  (v) // = For Flooring ( means -> floor integer number)
   (vi) % = For Reminder
'''

a = 10 ;
b = 10;
c = 20;

print("Sum :" , a + b);
print("Sub :" , c - a);
print("Div :" , c / a);
print("Mul :" , a * b);
print("Reminder : " , a % 3);
print("Floor: " , a // 3)

